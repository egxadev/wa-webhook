/**
 * Get all FAQs for a product
 */
export function getFAQsByProduct(product: 'silverstream' | 'stimel' | 'akusehat'): FAQQuestion[] {
  switch (product) {
    case 'silverstream':
      return silverstreamFAQs;
    case 'stimel':
      return stimelFAQs;
    case 'akusehat':
      return akusehatFAQs;
    default:
      return [];
  }
}

/**
 * Get FAQ by ID
 */
export function getFAQById(faqId: string): FAQQuestion | undefined {
  const allFAQs = [...silverstreamFAQs, ...stimelFAQs, ...akusehatFAQs];
  return allFAQs.find(faq => faq.id === faqId);
}

/**
 * Get FAQ by question text (case insensitive)
 */
export function getFAQByQuestion(questionText: string): FAQQuestion | undefined {
  const normalized = questionText.toLowerCase().trim();
  const allFAQs = [...silverstreamFAQs, ...stimelFAQs, ...akusehatFAQs];
  return allFAQs.find(faq => faq.question.toLowerCase() === normalized);
}
